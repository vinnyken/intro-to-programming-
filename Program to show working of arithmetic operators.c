// Working of arithmetic operators
#include <stdio.h>
int main()
{
    int a = 3,b = 7, c;
    
    c = a+b;
    printf("a+b = %d\n",c);
    c = a-b;
    printf("a-b = %d \n",c);
    c = a*b;
    printf("a*b = %d \n",c);
    c = a/b;
    printf("a/b = %d \n",c);
    c = a%b;
    printf("a%b = %d",c);
    
    return 0;
}
