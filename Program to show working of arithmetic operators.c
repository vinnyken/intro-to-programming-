// Working of arithmetic operators
#include <stdio.h>
int main()
{
    int a = 3,b = 7, d;
    
    d = a+b;
    printf("a+b = %d\n",d);
    d = a-b;
    printf("a-b = %d \n",d);
    d = a*b;
    printf("a*b = %d \n",d);
    d = a/b;
    printf("a/b = %d \n",d);
    d = a%b;
    printf("a%b = %d",d);
    
    return 0;
}
